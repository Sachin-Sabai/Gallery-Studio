if (typeof window !== "undefined") {
  const originalWarn = console.warn;
  console.warn = (...args) => {
    if (
      args[0] &&
      typeof args[0] === "string" &&
      args[0].includes("using deprecated parameters for the initialization function")
    ) {
      return;
    }
    originalWarn(...args);
  };
}
